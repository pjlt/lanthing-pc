#pragma once
#include <cstdint>
#include <optional>
#include <string>

namespace lt {

enum class CursorDataType { Icon, Bitmap, MonoChrome, Color, MaskedColor };

struct CursorInfo {
    std::optional<int32_t> preset;
    int32_t screen_w;
    int32_t screen_h;
    int32_t x;
    int32_t y;
    int32_t w;
    int32_t h;
    int32_t hot_x;
    int32_t hot_y;
    CursorDataType type;
    int32_t data_id;
    std::vector<uint8_t> data;
    int32_t pitch;
    bool visible;
};

} // namespace lt